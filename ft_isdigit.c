#include "libft.h"

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
    {
		return (1);
    }
	return (0);
}

int	sp_digit_pm(int c)
{
	if (ft_isdigit(c) || c == '+' || c == '-')
    {
    	return (1);
    }
    return (0);
}